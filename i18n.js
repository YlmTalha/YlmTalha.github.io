  const STORAGE_KEY = "site-lang";
  const supported = ["tr", "en"];

  function getSavedLang() {
    try {
      const saved = window.localStorage.getItem(STORAGE_KEY);
      if (saved && supported.includes(saved)) return saved;
    } catch (e) {}
    return null;
  }

  function detectLang() {
    const saved = getSavedLang();
    if (saved) return saved;
    const nav = (navigator.language || "tr").toLowerCase();
    return nav.startsWith("tr") ? "tr" : "en";
  }

  function applyLang(lang) {
    const dict = translations[lang] || translations.tr;
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (dict[key] !== undefined) el.textContent = dict[key];
    });

    document.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const key = el.getAttribute("data-i18n-html");
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria");
      if (dict[key] !== undefined) el.setAttribute("aria-label", dict[key]);
    });

    if (dict.page_title) document.title = dict.page_title;
    if (dict.page_description) {
      const meta = document.querySelector('meta[name="description"]');
      if (meta) meta.setAttribute("content", dict.page_description);
    }

    const toggleLabel = document.querySelector("[data-lang-toggle-label]");
    if (toggleLabel) toggleLabel.textContent = lang === "tr" ? "EN" : "TR";

    try {
      window.localStorage.setItem(STORAGE_KEY, lang);
    } catch (e) {}
  }

  let currentLang = detectLang();
  applyLang(currentLang);

  document.addEventListener("DOMContentLoaded", () => {
    const toggle = document.querySelector("[data-lang-toggle]");
    toggle?.addEventListener("click", () => {
      currentLang = currentLang === "tr" ? "en" : "tr";
      applyLang(currentLang);
    });
  });
